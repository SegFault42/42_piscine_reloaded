int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (tab[x] != 0)
	{
		if (f(tab[x]) == 1)
			++i;
		++x;
	}
	return (i);
}
